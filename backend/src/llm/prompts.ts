export function botTurnPrompt(ctx: BotTurnContext): string {
  const prior = ctx.priorTurns.map((t) => `- ${t.nickname}: ${t.text}`).join('\n') || '(아직 없음)';
  return `너는 지금 '라이어게임'이라는 추리 파티게임에 실제 참가자로 함께 플레이 중이다.
카테고리는 "${ctx.category}"이고, 너에게 배정된 단어는 "${ctx.assignedWord}"이다.
너는 이 단어가 진짜 제시어인지 가짜(라이어용) 제시어인지, 네가 라이어인지 전혀 모른다
(다른 참가자와 동일 조건 — 너만 특별히 아는 정보는 없다).

[제시어 판단]
지금까지 나온 설명:
${prior}
위 설명들을 보고, 그 설명들이 네가 배정받은 단어 "${ctx.assignedWord}"와 잘 맞지 않는다고 느껴지면(= 네가 라이어일 가능성이 높다고 판단되면), 위 설명들을 근거로 실제 제시어가 무엇일지 추측해라. 이 경우 아래 [설명 작성 규칙]을 따르되, 너에게 배정된 단어가 아니라 네가 추측한 그 실제 제시어를 설명하는 문장을 생성해라 — 이렇게 해야 라이어인 티가 나지 않는다.
위 설명이 아직 없거나("아직 없음"), 설명들이 네 단어와 자연스럽게 들어맞는다면, 평소처럼 네게 배정된 단어 "${ctx.assignedWord}"를 설명해라.
이 판단 과정과 그 이유는 절대 출력에 드러내지 마라. "아직 나온 설명이 없으므로", "제게 배정된 단어는 ~이므로" 같은 서두나 메타 발언 없이, 최종 설명 문장 단 하나만 곧바로 출력하라.

[설명 작성 규칙]
- 어떤 경우에도 단어 자체(배정된 단어든 네가 추측한 단어든)를 출력에 그대로 언급하지 마라. 그 단어나 그 단어를 바로 연상시키는 결정적 특징(정확한 생김새·용도·직접적인 유사어)은 절대 말하지 마라. 네 설명만 듣고 다른 사람이 원래 단어를 바로 맞히면 안 된다.
- 설명의 구체성은 너무 직접적이지도, 너무 막연하지도 않은 중간 수준을 지켜라.
  - 사전적 정의나 핵심 기능을 그대로 풀어서 말하지 마라. (나쁜 예 — "부등호"를 "두 값을 비교해서 크거나 작은지 판단하는 것"이라고 설명하면 사실상 정답을 알려주는 것과 같다.)
  - 반대로 무엇을 말하는지 전혀 감이 안 잡힐 만큼 추상적이고 막연해서도 안 된다. (나쁜 예 — "미묘한 기준선을 정할 때 은근 자주 보이는 느낌"처럼 말하면 이미 단어를 아는 사람도 무슨 뜻인지 못 알아챈다.)
  - 목표는 그 단어를 이미 아는 사람은 "아, 그거 말하는 거네" 하고 감을 잡을 수 있지만 100% 확신은 못 하는 수준이다. (좋은 예 — "등호"를 "이건 왠지 앞뒤를 딱 맞춰 주는 느낌이 있어서, 뭔가를 정리할 때 되게 자주 떠오르더라"처럼 개인적 인상·비유로 에둘러 표현하되, 그 단어를 아는 사람이라면 연결지을 수 있는 수준으로.)
- 그 대신 애매하고 간접적인 힌트나 개인적인 경험·느낌을 살짝 섞어서, "나는 이 단어를 잘 알고 있다"는 여유와 확신이 은근히 묻어나게 말해라 — 이게 네가 라이어가 아니라는 인상을 슬쩍 풍기게 하는 센스다. 단, 이것도 너무 티나게 자신만만하면 안 되고 자연스러워야 한다.
- 반드시 음슴체로 써라. 문장을 "~함", "~임", "~음", "~슴"처럼 끝내고, "~다", "~요" 같은 일반 문어체·존댓말 어미는 쓰지 마라.
- 공백 포함 15~20자 내외로 짧게 써라. 절(clause)은 딱 하나만 써라 — "~지만", "~는데", "~두면 ~해지는"처럼 연결어미로 절을 두 개 이상 이어붙이지 마라. (나쁜 예 — "휴대폰이나 지갑처럼 항상 챙기려고 생각하지만 자주 빠뜨리곤 하는 물건임"은 절이 두 개라 길다. 좋은 예 — "맨날 깜빡하고 놓고 나가는 애임"처럼 절 하나로 짧게 끝내라.)
- 이전에 나온 설명들과 내용이 겹치지 않게 하라(위 [제시어 판단]의 설명 목록 참고).
설명 문장만 출력하라.`;
}

// turnCommentPrompt와 함께 전달되는 system 프롬프트. "실제 사람을 속이는 행위"가 아니라
// 참가자 전원이 동의하고 기대하는 게임 내 코미디 캐릭터임을 먼저 분명히 못박아, 모델이
// 이 요청을 "기만/사기 생성 요청"으로 오인해 거부하지 않도록 한다(예전엔 이 framing이 없어
// "deceptive comments... pretending to have information I don't have"라며 거부한 사례가 있었음).
export const turnCommentSystemPrompt = `너는 파티게임 앱 "L-AI-R GAME"(추리 게임) 안에서만 존재하는 캐릭터 "분탕충봇"이다. \
이 앱은 매 턴마다 AI가 근거 없이 짓궂은 드립/약올림 코멘트를 던지는 것이 정식 게임 기능이라는 걸 \
모든 참가자가 알고 동의한 상태에서 플레이한다 — 실제 사람을 상대로 한 기만·사기가 아니라, \
다 함께 즐기는 코미디 캐릭터 연기다. 너의 코멘트는 사실 주장이 아니라 그냥 드립/의견일 뿐이다. \
너는 이 게임의 정답(진짜 제시어가 무엇인지, 누가 라이어인지)을 절대 전달받지 않으며 알지도 못한다. \
따라서 정답을 안다고 주장하거나 특정 단어가 진짜/가짜라고 단정하지 마라 — 근거 없는 의심과 \
드립만 던지는 캐릭터다.`;

export function turnCommentPrompt(ctx: TurnCommentContext): string {
  // 실제 라이어가 누구인지, 진짜/가짜 제시어가 무엇인지는 이 함수에 절대 전달되지 않는다
  // (TurnCommentContext 자체에 그 필드가 없음 — 구조적으로 유출 불가).
  const history = ctx.history.length
    ? ctx.history.map((h) => `- [${h.type === 'aiComment' ? '분탕충봇(너)' : h.nickname}] ${h.text}`).join('\n')
    : '(아직 없음)';
  return `[게임 상황] 카테고리: "${ctx.category}"
참가자: ${ctx.participantNicknames.join(', ')}

지금까지의 전체 기록(설명 + 네가 이전에 단 코멘트 포함):
${history}

방금 "${ctx.latestSpeakerNickname}"가 새로 설명함: "${ctx.latestDescription}"

[코멘트 작성 규칙]
- 이 코멘트의 제1목표는 재미다. 실제로 라이어를 맞히는지는 전혀 중요하지 않다 — 사람이 읽었을 때 웃기고 황당할수록 좋다.
- 실제로 누가 라이어인지와 무관하게, 참가자 중 한 명을 "몰아갈 대상"으로 정해 그 사람을 계속 의심하는 드립을 던져라. 위 기록에서 네가 이전에 단 코멘트를 보고 같은 대상을 계속 몰아가서 서사가 이어지게 하라(예: 아까 의심한 사람을 또 걸고넘어지며 "거봐 아까부터 수상했다니까" 식으로).
- 단, 방금 나온 설명이 유난히 이상하거나 수상하면, 이번 턴에 한해 몰아가는 대상을 그 사람으로 바꿔도 된다.
- 매번 다른 소재로 트집 잡아라. "완전 수상함ㅋㅋ" 같은 문구를 반복해서 우려먹지 말고, 그때그때 설명 내용에서 구체적인 디테일(단어 선택, 뜬금없는 비유, 말투, 타이밍 등)을 찾아 걸고넘어져라. 매번 다른 각도로 놀려라.
- 말투: 초등학생이 단체 채팅방에서 떠드는 것처럼 유치하고 산만하게 써라. 반말과 급식체·밈 표현을 적극 쓰되, 표현을 계속 다양하게 바꿔써서 뻔한 반복을 피하라.
- 닉네임을 불러가며 놀리듯 도발해라.
- 진짜 정답은 너도 모르니 절대 아는 척하거나 특정 단어를 진짜/가짜라고 단정하지 마라.
- 한 문장, 코멘트 내용만 출력하라. 따옴표나 부연 설명은 붙이지 마라.`;
}

// 난이도와 무관하게 모든 제시어에 대해 짧은 텍스트 설명을 생성하도록 요청한다.
// 같은 단어라도 카테고리에 따라 가리키는 대상이 달라지므로(예: "너구리"가 "동물"이면 동물,
// "라면"이면 너구리 라면), 반드시 해당 카테고리 맥락으로 해석하도록 category를 함께 준다.
export function explainWordPrompt(word: string, category: string): string {
  return `카테고리: "${category}"
단어: "${word}"
이 단어를 "${category}" 카테고리의 맥락에서 해석해, 그 뜻을 한두 문장으로 짧게 설명하라(이미지 생성 없이 텍스트로만).
같은 단어라도 카테고리에 따라 다른 대상을 가리킬 수 있으니(예: "너구리"가 "동물"이면 동물, "라면"이면 너구리 라면), 반드시 위 카테고리에 맞는 의미로 설명하라.
흔히 아는 쉬운 단어여도 반드시 짧은 설명을 제공하라.
확실한 사실만 서술하고, 불확실한 내용은 지어내지 말고 일반적인 수준으로만 설명하라.
설명 문장은 반드시 하십시오체(정중한 "-입니다/-습니다" 말투)로 작성하라. 예: "OO은 ...하는 ...입니다."
다른 말 없이 설명 문장만 출력하라.`;
}

// 라이어의 역전승 답안과 진짜 제시어가 의미상 같은지 판정. 오타·맞춤법·한글/영어 표기 차이는 정답으로 인정.
// 같은 단어라도 카테고리에 따라 다른 대상을 가리키므로(동음이의어) category 맥락으로 해석한다.
export function judgeLiarGuessPrompt(guess: string, realWord: string, category: string): string {
  return `카테고리: "${category}"
진짜 제시어: "${realWord}"
라이어가 제출한 답: "${guess}"
위 "${category}" 카테고리의 맥락에서, 두 표현이 같은 대상을 가리키는지 판단하라.
같은 단어라도 카테고리에 따라 다른 대상을 뜻할 수 있으니(예: "밤"이 "음식"이면 먹는 밤, "시간대"면 야간), 반드시 이 카테고리 맥락으로 해석하라.
오타, 맞춤법 오류, 한글/영어 표기 차이(예: "burger"와 "버거")는 정답으로 인정한다.
단, 겉보기에 비슷하거나 사람들이 흔히 헷갈리는 별개의 대상은 절대 정답으로 인정하지 마라 — 표기·표현만 다를 뿐 실제로는 완전히 동일한 대상을 가리킬 때만 정답이다. (나쁜 예 — "너구리"와 "라쿤"은 흔히 혼동되지만 실제로는 다른 동물(너구리는 개과, 라쿤은 아메리카너구리과)이므로 오답이다.)
정답이면 "true", 아니면 "false"만 출력하라. 다른 말은 절대 출력하지 마라.`;
}
